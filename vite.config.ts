import { defineConfig } from "vite";
import react from "@vitejs/plugin-react";

// @ts-expect-error process is a nodejs global
const host = process.env.TAURI_DEV_HOST;

// https://vite.dev/config/
export default defineConfig(async () => ({
  plugins: [react()],

  // Keep the heavy 3D stack out of the entry chunk so it is only fetched the
  // first time a model viewer is shown (see LazyViewer3D). Without this the
  // ~1.5 MB three/drei bundle was parsed at launch and froze the app.
  build: {
    rollupOptions: {
      output: {
        manualChunks: {
          "vendor-three": [
            "three",
            "@react-three/fiber",
            "@react-three/drei",
          ],
        },
      },
    },
  },

  // Vite options tailored for Tauri development and only applied in `tauri dev` or `tauri build`
  //
  // 1. prevent Vite from obscuring rust errors
  clearScreen: false,
  // 2. tauri expects a fixed port, fail if that port is not available
  server: {
    port: 1420,
    strictPort: true,
    host: host || false,
    hmr: host
      ? {
          protocol: "ws",
          host,
          port: 1421,
        }
      : undefined,
    watch: {
      // 3. tell Vite to ignore watching `src-tauri`
      ignored: ["**/src-tauri/**"],
    },
  },
}));
